package Model;

public class aModel {
	private String uname,apwd;

	public aModel() {
		super();
	}

	public aModel(String uname, String apwd) {
		super();
		this.uname = uname;
		this.apwd = apwd;
	}

	public String getUname() {
		return uname;
	}

	public void setUname(String uname) {
		this.uname = uname;
	}

	public String getApwd() {
		return apwd;
	}

	public void setApwd(String apwd) {
		this.apwd = apwd;
	}
	
}
